bool completion(int grid[25][25], int &row, int &col)
{
    for (row; row < 25; ++row)
    {
        for (col = 0; col < 25; ++col)
        {
            if (grid[row][col] == 0)
            {
                return 0;
            }
        }
    }
    return 1;
}

bool safe(int grid[25][25], int row, int col, int num)
{
    for (int i = 0; i < 25; ++i)
    {
        if (grid[row][i] == num || grid[i][col] == num)
        {
            return 0;
        }
    }
    int br = row - row % 5;
    int bc = col - col % 5;
    for (int r = 0; r < 5; ++r)
    {
        for (int c = 0; c < 5; ++c)
        {
            if (grid[r + br][c + bc] == num)
            {
                return 0;
            }
        }
    }
    return 1;
}

bool Solve(int grid[25][25], int row)
{
    int col;
    if (completion(grid, row, col))
    {
        return 1;
    }
    for (int num = 1; num <= 25; ++num)
    {
        if (safe(grid, row, col, num))
        {
            grid[row][col] = num;
            if (Solve(grid, row))
            {
                return 1;
            }
            grid[row][col] = 0;
        }
    }
    return 0;
}

